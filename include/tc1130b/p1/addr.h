/* block "P1 CD" of TriCore TC1130B (8 SFRs) */

#ifndef _HAVE_TRICORE_P1_ADDRESSES_H_
#define _HAVE_TRICORE_P1_ADDRESSES_H_

#define P1_OUT_ADDR           0xF0000D10     /* "Port 1 Data Output Register" */
#define P1_IN_ADDR            0xF0000D14     /* "Port 1 Data Input Register" */
#define P1_DIR_ADDR           0xF0000D18     /* "Port 1 Direction Register" */
#define P1_OD_ADDR            0xF0000D1C     /* "Port 1 Open Drain Mode Register" */
#define P1_PUDSEL_ADDR        0xF0000D28     /* "Port 1 Pull up/down Select Register" */
#define P1_PUDEN_ADDR         0xF0000D2C     /* "Port 1 Pull up/down Enable Register" */
#define P1_ALTSEL0_ADDR       0xF0000D44     /* "Port 1 Alternate Select Register 0" */
#define P1_ALTSEL1_ADDR       0xF0000D48     /* "Port 1 Alternate Select Register 1" */


#endif /* _HAVE_TRICORE_P1_ADDRESSES_H_ (block "P1 CD") */


