/* block "P0 CD" of TriCore TC1130B (8 SFRs) */

#ifndef _HAVE_TRICORE_P0_ADDRESSES_H_
#define _HAVE_TRICORE_P0_ADDRESSES_H_

#define P0_OUT_ADDR           0xF0000C10     /* "Port 0 Data Output Register" */
#define P0_IN_ADDR            0xF0000C14     /* "Port 0 Data Input Register" */
#define P0_DIR_ADDR           0xF0000C18     /* "Port 0 Direction Register" */
#define P0_OD_ADDR            0xF0000C1C     /* "Port 0 Open Drain Mode Register" */
#define P0_PUDSEL_ADDR        0xF0000C28     /* "Port 0 Pull up/down Select Register" */
#define P0_PUDEN_ADDR         0xF0000C2C     /* "Port 0 Pull up/down Enable Register" */
#define P0_ALTSEL0_ADDR       0xF0000C44     /* "Port 0 Alternate Select Register 0" */
#define P0_ALTSEL1_ADDR       0xF0000C48     /* "Port 0 Alternate Select Register 1" */


#endif /* _HAVE_TRICORE_P0_ADDRESSES_H_ (block "P0 CD") */


