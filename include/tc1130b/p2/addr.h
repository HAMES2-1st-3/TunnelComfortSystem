/* block "P2 CD" of TriCore TC1130B (8 SFRs) */

#ifndef _HAVE_TRICORE_P2_ADDRESSES_H_
#define _HAVE_TRICORE_P2_ADDRESSES_H_

#define P2_OUT_ADDR           0xF0000E10     /* "Port 2 Data Output Register" */
#define P2_IN_ADDR            0xF0000E14     /* "Port 2 Data Input Register" */
#define P2_DIR_ADDR           0xF0000E18     /* "Port 2 Direction Register" */
#define P2_OD_ADDR            0xF0000E1C     /* "Port 2 Open Drain Mode Register" */
#define P2_PUDSEL_ADDR        0xF0000E28     /* "Port 2 Pull up/down Select Register" */
#define P2_PUDEN_ADDR         0xF0000E2C     /* "Port 2 Pull up/down Enable Register" */
#define P2_ALTSEL0_ADDR       0xF0000E44     /* "Port 2 Alternate Select Register 0" */
#define P2_ALTSEL1_ADDR       0xF0000E48     /* "Port 2 Alternate Select Register 1" */


#endif /* _HAVE_TRICORE_P2_ADDRESSES_H_ (block "P2 CD") */


