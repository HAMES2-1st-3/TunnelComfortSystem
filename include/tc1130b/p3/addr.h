/* block "P3 CD" of TriCore TC1130B (8 SFRs) */

#ifndef _HAVE_TRICORE_P3_ADDRESSES_H_
#define _HAVE_TRICORE_P3_ADDRESSES_H_

#define P3_OUT_ADDR           0xF0000F10     /* "Port 3 Data Output Register" */
#define P3_IN_ADDR            0xF0000F14     /* "Port 3 Data Input Register" */
#define P3_DIR_ADDR           0xF0000F18     /* "Port 3 Direction Register" */
#define P3_OD_ADDR            0xF0000F1C     /* "Port 3 Open Drain Mode Register" */
#define P3_PUDSEL_ADDR        0xF0000F28     /* "Port 3 Pull up/down Select Register" */
#define P3_PUDEN_ADDR         0xF0000F2C     /* "Port 3 Pull up/down Enable Register" */
#define P3_ALTSEL0_ADDR       0xF0000F44     /* "Port 3 Alternate Select Register 0" */
#define P3_ALTSEL1_ADDR       0xF0000F48     /* "Port 3 Alternate Select Register 1" */


#endif /* _HAVE_TRICORE_P3_ADDRESSES_H_ (block "P3 CD") */


