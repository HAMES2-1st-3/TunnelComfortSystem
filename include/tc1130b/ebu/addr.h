#define EBU_SDRMCON1_ADDR     0xF8000058     /* "EBU_LMB SDRAM Control Register 1" */
#define EBU_SDRMOD0_ADDR      0xF8000060     /* "EBU_LMB SDRAM Mode Register 0" */
#define EBU_SDRMOD1_ADDR      0xF8000068     /* "EBU_LMB SDRAM Mode Register 1" */
#define EBU_SDRSTAT0_ADDR     0xF8000070     /* "EBU_LMB SDRAM Status Register 0" */
