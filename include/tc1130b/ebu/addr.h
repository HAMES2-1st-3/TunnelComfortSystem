#define EBU_EMUOVL_ADDR       0xF8000178     /* "EBU_LMB Emulator Overlay Register" */
#define EBU_USERCON_ADDR      0xF8000190     /* "Test/Configuration Register" */


#endif /* _HAVE_TRICORE_EBU_ADDRESSES_H_ (block "EBU_LMB4") */


