/* block "IIC CD" of TriCore TC1130B (10 SFRs) */

#ifndef _HAVE_TRICORE_IIC_ADDRESSES_H_
#define _HAVE_TRICORE_IIC_ADDRESSES_H_

#define IIC_CLC_ADDR          0xF0100600     /* "IIC Clock Control Register" */
#define IIC_PISEL_ADDR        0xF0100604     /* "IIC Input Select Register" */
#define IIC_ID_ADDR           0xF0100608     /* "IIC Module Identification Register" */
#define IIC_SYSCON_ADDR       0xF0100610     /* "System Control Register" */
#define IIC_BUSCON_ADDR       0xF0100614     /* "IIC BUS Control Register" */
#define IIC_RTB_ADDR          0xF0100618     /* "Reset Transmit Buffer" */
#define IIC_WHBSYSCON_ADDR    0xF0100620     /* "IIC Write Hardware Modifiied System Control" */
#define IIC_XP2SRC_ADDR       0xF01006F4     /* "IIC Service Request Control Register 2" */
#define IIC_XP1SRC_ADDR       0xF01006F8     /* "IIC Service Request Control Register 1" */
#define IIC_XP0SRC_ADDR       0xF01006FC     /* "IIC Service Request Control Register 0" */


#endif /* _HAVE_TRICORE_IIC_ADDRESSES_H_ (block "IIC CD") */


