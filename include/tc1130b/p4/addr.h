/* block "P4 CD" of TriCore TC1130B (8 SFRs) */

#ifndef _HAVE_TRICORE_P4_ADDRESSES_H_
#define _HAVE_TRICORE_P4_ADDRESSES_H_

#define P4_OUT_ADDR           0xF0001010     /* "Port 4 Data Output Register" */
#define P4_IN_ADDR            0xF0001014     /* "Port 4 Data Input Register" */
#define P4_DIR_ADDR           0xF0001018     /* "Port 4 Direction Register" */
#define P4_OD_ADDR            0xF000101C     /* "Port 4 Open Drain Mode Register" */
#define P4_PUDSEL_ADDR        0xF0001028     /* "Port 4 Pull up/down Select Register" */
#define P4_PUDEN_ADDR         0xF000102C     /* "Port 4 Pull up/down Enable Register" */
#define P4_ALTSEL0_ADDR       0xF0001044     /* "Port 4 Alternate Select Register 0" */
#define P4_ALTSEL1_ADDR       0xF0001048     /* "Port 4 Alternate Select Register 1" */


#endif /* _HAVE_TRICORE_P4_ADDRESSES_H_ (block "P4 CD") */


